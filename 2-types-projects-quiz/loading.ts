{
  /**
   * Print Loading State
   */
  type LoadingState = {
    state: 'loading';
  };

  type SuccessState = {
    state: 'success';
    response: {
      body: string;
    };
  };

  type FailState = {
    state: 'fail';
    reason: string;
  };

  type ResourceLoadState = LoadingState | SuccessState | FailState;

  function printLoginState(loadState: ResourceLoadState) {
    if (loadState.state === 'fail') console.log(`ðŸ˜± ${loadState.reason}`);
    else if (loadState.state === 'loading') console.log('ðŸ‘€ loading...');
    else console.log(`ðŸ˜ƒ ${loadState.response.body}`);
  }

  printLoginState({ state: 'loading' }); // ðŸ‘€ loading...
  printLoginState({ state: 'success', response: { body: 'loaded' } }); // ðŸ˜ƒ loaded
  printLoginState({ state: 'fail', reason: 'no network' }); // ðŸ˜± no network
}
